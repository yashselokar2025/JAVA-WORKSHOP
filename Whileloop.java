public class Whileloop {
    public static void main(String[] args) {
        int i = 1;
        while(i <= 10){
            System.out.println("The value of i is: " + i);
            i++;
        }

        int j = 5;
        while(j >= 1){
            System.out.println(j);
            j--;
        }
    }    
}
